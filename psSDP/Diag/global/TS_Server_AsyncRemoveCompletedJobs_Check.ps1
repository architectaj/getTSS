# Rule to determine whether a DWORD registry entry that is named AsyncRemoveCompletedJobs existst and set correctly.
# http://support.microsoft.com/kb/957871
#
# Created: 4/23/2012
# Author: Jonathan Randall
#******************************************************************************************************************
Import-LocalizedData -BindingVariable LocalizedMessages 
Write-DiagProgress -Activity $LocalizedMessages.PROGRESSBAR_ID_AsyncRemoveCompletedJobs_CHECK -Status $LocalizedMessages.PROGRESSBAR_ID_AsyncRemoveCompletedJobs_CHECKDesc

"--> Entered TS_Server_AsyncRemoveCompletedJobs_Check.ps1" | WriteTo-StdOut

$RootCauseDetected=$false
$Error.Clear()
if (Test-Path HKLM:\Software\Microsoft\MSCRM){
	$MSCRMKey = Get-Item HKLM:\Software\Microsoft\MSCRM
	if ($MSCRMKey -ne $null){
		$server_version = $MSCRMKey.GetValue("CRM_Server_Version").Substring(0,1)
		if ($server_version -eq "4"){
			#V4 Rule Only
			$crm_key_properties = Get-ItemProperty HKLM:\Software\Microsoft\MSCRM
			if ($crm_key_properties.AsyncRemoveCompletedJobs -eq $null -or $crm_key_properties.AsyncRemoveCompletedJobs -eq 0 ){
				#Either DWORD Value is missing entirely or set to disabled so flag issue
				$RootCauseDetected=$true
				Write-GenericMessage -RootCauseID "RC_AsyncRemoveCompletedJobs_Missing" -Verbosity "Warning" -SolutionTitle "The workflow expansion task registry key preventing AsyncOperationBase table growth is missing" -PublicContentURL "http://support.microsoft.com/kb/957871"  -ProcessName "W3WP.exe" 
			}
			
		}
	}
}
if ($RootCauseDetected -eq $true){
	Update-DiagRootCause -Id "RC_AsyncRemoveCompletedJobs_Missing" -Detected $true 
	if ($Error.Count -gt 0){
		("ERROR=" + $Error) | WriteTo-StdOut
	}
}else{
	Update-DiagRootCause -Id "RC_AsyncRemoveCompletedJobs_Missing" -Detected $false 
}
"<-- Exited TS_Server_AsyncRemoveCompletedJobs_Check.ps1" | WriteTo-StdOut
