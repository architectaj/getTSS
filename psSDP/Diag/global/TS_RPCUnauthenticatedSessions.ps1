Import-LocalizedData -BindingVariable ScriptStrings
Display-DefaultActivity -Rule -RuleNumber 2387

$RuleApplicable = $true
$RootCauseDetected = $false
$RootCauseName = "RC_RPCUnauthenticatedSessions"
$PublicContent = "http://blogs.msdn.com/b/distributedservices/archive/2008/11/12/troubleshooting-msdtc-issues-with-the-dtcping-tool.aspx"
$InternalContent = "https://vkbexternal.partners.extranet.microsoft.com/VKBWebService/ViewContent.aspx?scid=B;EN-US;2506972"
$Verbosity = "Warning"
$Visibility = "3"
$SupportTopicsID = "8077"
$InformationCollected = new-object PSObject 
$RPCAuthenticatePath = "HKLM:\SOFTWARE\Policies\Microsoft\Windows NT\Rpc"

# ***************************
# Data Gathering
# ***************************

Function GetRPCAuthenticateValue
{
	if(Test-Path $RPCAuthenticatePath)
	{
		return (Get-ItemProperty ($RPCAuthenticatePath)).RestrictRemoteClients
	}
	else
	{
		return $null
	}
}

# **************
# Detection Logic
# **************
#WriteTo-StdOut -Color Magenta -DebugOnly "Detection Logic"

$RPCAuthenticateValue = GetRPCAuthenticateValue

if(($RPCAuthenticateValue -eq 2) -or ($RPCAuthenticateValue -eq 1)) #_# added value=1
{
	$InformationCollected | add-member -membertype noteproperty -name "RPC Authenticate Registry Key Name" -value "$RPCAuthenticatePath\RestrictRemoteClients" 
	$InformationCollected | add-member -membertype noteproperty -name "RPC Authenticate Registry Key Value" -value $RPCAuthenticateValue 
	$RootCauseDetected = $true
}
		

# *********************
# Root Cause processing
# *********************

if ($RuleApplicable)
{
	if ($RootCauseDetected)
	{
		# Red/ Yellow Light
		Update-DiagRootCause -id $RootCauseName -Detected $true
		Write-GenericMessage -RootCauseId $RootCauseName -PublicContentURL $PublicContent -InformationCollected $InformationCollected -Verbosity $Verbosity -Visibility $Visibility -SupportTopicsID $SupportTopicsID -SolutionTitle $ScriptStrings.ID_RPCUnauthenticatedSessions_SD
	}
	else
	{
		# Green Light
		Update-DiagRootCause -id $RootCauseName -Detected $false
	}
}


